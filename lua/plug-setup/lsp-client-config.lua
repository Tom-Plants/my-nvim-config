local function read_files()
    local configs = {}

    local read_dir = './../ls-setup/'
    -- 使用 Lua 的文件操作函数读取目录
    local dir = io.popen('ls -p "' .. read_dir .. '" | grep -v /')
    for file in dir:lines() do
        local filepath = 'ls-setup/' .. file
        filepath = filepath:match("(.+)%..+")
        -- 将文件路径和内容添加到结果表中
        table.insert(configs, {
            filepath = filepath,
        })
    end


    -- 返回配置文件的结果表
    return configs
end

local ls_conf = read_files()

for _, config in ipairs(ls_conf) do
    require(config.filepath)
end

